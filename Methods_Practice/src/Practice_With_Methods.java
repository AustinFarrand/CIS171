public class Practice_With_Methods {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int sum =0;
        for(int i = 0;i <= 10; i++  ){
            sum += i;
            
        }
        int sum1=0;
        for (int j = 10; j<=20; j++){
            sum1+=j;
        }
        
        int sum2=0;
        for (int k = 35; k<=40; k++){
            sum2+=k;
        }
        System.out.println(sum);
        System.out.println(sum1);
        System.out.println(sum2);
    }
    
}
